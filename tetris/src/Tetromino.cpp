#include "Tetromino.h"

// ============================================================
// 形状数据定义
// 每个方块由4个格子组成，坐标 (row, col) 相对于方块 4x4 矩阵的左上角
// ============================================================
const std::array<std::array<std::array<std::pair<int, int>, 4>, 4>, 7> Tetromino::SHAPES = {{
    // === I-方块（青色） ===
    {{
        {{ {1,0}, {1,1}, {1,2}, {1,3} }},  // 旋转 0
        {{ {0,2}, {1,2}, {2,2}, {3,2} }},  // 旋转 1
        {{ {2,0}, {2,1}, {2,2}, {2,3} }},  // 旋转 2
        {{ {0,1}, {1,1}, {2,1}, {3,1} }},  // 旋转 3
    }},
    // === O-方块（黄色）- 保持不变 ===
    {{
        {{ {0,1}, {0,2}, {1,1}, {1,2} }},
        {{ {0,1}, {0,2}, {1,1}, {1,2} }},
        {{ {0,1}, {0,2}, {1,1}, {1,2} }},
        {{ {0,1}, {0,2}, {1,1}, {1,2} }},
    }},
    // === T-方块（紫色） ===
    {{
        {{ {0,1}, {1,0}, {1,1}, {1,2} }},  // 旋转 0
        {{ {0,1}, {1,1}, {1,2}, {2,1} }},  // 旋转 1
        {{ {1,0}, {1,1}, {1,2}, {2,1} }},  // 旋转 2
        {{ {0,1}, {1,0}, {1,1}, {2,1} }},  // 旋转 3
    }},
    // === S-方块（绿色） ===
    {{
        {{ {0,1}, {0,2}, {1,0}, {1,1} }},  // 旋转 0
        {{ {0,1}, {1,1}, {1,2}, {2,2} }},  // 旋转 1
        {{ {1,1}, {1,2}, {2,0}, {2,1} }},  // 旋转 2
        {{ {0,0}, {1,0}, {1,1}, {2,1} }},  // 旋转 3
    }},
    // === Z-方块（红色） ===
    {{
        {{ {0,0}, {0,1}, {1,1}, {1,2} }},  // 旋转 0
        {{ {0,2}, {1,1}, {1,2}, {2,1} }},  // 旋转 1
        {{ {1,0}, {1,1}, {2,1}, {2,2} }},  // 旋转 2
        {{ {0,1}, {1,0}, {1,1}, {2,0} }},  // 旋转 3
    }},
    // === J-方块（蓝色） ===
    {{
        {{ {0,0}, {1,0}, {1,1}, {1,2} }},  // 旋转 0
        {{ {0,1}, {0,2}, {1,1}, {2,1} }},  // 旋转 1
        {{ {1,0}, {1,1}, {1,2}, {2,2} }},  // 旋转 2
        {{ {0,1}, {1,1}, {2,0}, {2,1} }},  // 旋转 3
    }},
    // === L-方块（橙色） ===
    {{
        {{ {0,2}, {1,0}, {1,1}, {1,2} }},  // 旋转 0
        {{ {0,1}, {1,1}, {2,1}, {2,2} }},  // 旋转 1
        {{ {1,0}, {1,1}, {1,2}, {2,0} }},  // 旋转 2
        {{ {0,0}, {0,1}, {1,1}, {2,1} }},  // 旋转 3
    }},
}};

// ============================================================
// 颜色定义
// ============================================================
const std::array<Color, 7> Tetromino::COLORS = {{
    COLOR_CYAN,       // I
    COLOR_YELLOW,     // O
    COLOR_PURPLE,     // T
    COLOR_GREEN,      // S
    COLOR_RED,        // Z
    COLOR_BLUE,       // J
    COLOR_ORANGE,     // L
}};

// ============================================================
// 构造函数
// ============================================================
Tetromino::Tetromino(TetrominoType type)
    : m_type(type)
    , m_rotation(0)
    , m_row(0)
    , m_col(getSpawnCol())
{
}

// ============================================================
// 顺时针旋转
// ============================================================
void Tetromino::rotateCW() {
    m_rotation = (m_rotation + 1) % 4;
}

// ============================================================
// 逆时针旋转
// ============================================================
void Tetromino::rotateCCW() {
    m_rotation = (m_rotation + 3) % 4;
}

// ============================================================
// 获取当前旋转状态下4个格子的偏移坐标
// ============================================================
const std::array<std::pair<int, int>, 4>& Tetromino::getBlockOffsets() const {
    return SHAPES[static_cast<int>(m_type)][m_rotation];
}

// ============================================================
// 移动方块
// ============================================================
void Tetromino::move(int dr, int dc) {
    m_row += dr;
    m_col += dc;
}

// ============================================================
// 获取方块颜色
// ============================================================
Color Tetromino::getColor() const {
    return COLORS[static_cast<int>(m_type)];
}

// ============================================================
// 获取初始生成列偏移（居中）
// ============================================================
int Tetromino::getSpawnCol() const {
    return (BOARD_COLS - TETROMINO_SIZE) / 2;
}
